										<p>Overdraft facility: This is a revolving facility and the interest will be charged on the amount that will be utilised. You can operate this account throughout the term that is stipulated. During this period, you can withdraw the amount whenever you need and deposit back when the funds are available. This is similar to a current account, but the difference is the funds in the current account is your own funds for which you do not pay any interest, whereas in an overdraft facility you will be utilising the funds provided by the financial institution for which you will have to pay a cost.</p>
										<p>Partial repayment: In this option, there is no repayment pattern drawn by the bank/financial institution which has to be followed. You can make a payment whenever funds are available. This is again suitable for persons who do not have a regular income. This is suitable for businessmen who do not have a regular pattern for income.</p>
										<p>EMI only for interest: You will be paying the interest whenever due and the principal amount will be paid at the end of the tenure. This way, you will be avoiding compounding of interest.</p>
										<h3>Margin</h3>
										<p>A margin is set between 25% to 35% for Gold Loans. If the LTV Ratio is high, then the risk factor for the lenders increases. So, they consider various factors while fixing the LTV. If you are a long-standing customer of the bank and have a good relationship, you can negotiate on the margin. Otherwise, the margin set is 25% to 35%. If in case the borrower fails to pay both the principal and the interest on the due date, then the interest keeps accumulating which results in the erosion of margin, which increases the risk factor for the borrowers.</p>
										<h3>Gold Accepted for Pledge</h3>
										<p>Gold only in the form of ornaments is accepted as security. Gold bars and coins are not accepted as security. However, Gold coins purchased from banks which are of 24-carat purity are accepted, but there is a restriction on the quantity. Gold coins only up to 50 grams can be accepted as security as per the guidelines of the Reserve Bank of India.</p>
										<p>If you want a higher quantum, then it can be combined security of both the gold coins and the ornaments.</p>
										<h3>Purity of Gold</h3>
										<p>Before approaching a lender, you should know the purity of the gold that you are going to pledge. Banks/financial institutions accept only gold with 18 to 22-carat purity. For gold coins, the purity level expected is 24 carats. It is better you pledge ornaments with no gems studded in them since the weight of the gems will not be considered and you may end up getting a lower loan amount.</p>

